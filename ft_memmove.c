#include "libft.h"
#include <stdlib.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t		i;
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	if (src == NULL && dst == NULL)
		return (dst);
	if (dst <= src && dst + len >= src)
	{
		i = 0;
		while (i < len)
		{
			d[i] = s[i];
			i++;
		}
	}
	else
	{
		i = len;
		while (i--)
			d[i] = s[i];
	}
	return (dst);
}
